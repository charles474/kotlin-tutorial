fun main() {
    var number = 10

    // Increment the number and then return it's new value
    println(++number) // 11

    // Return the current value and then increment the number.
    println(number++) // 11

    // Print the new value of the variable
    println(number) // 12

    // THE REVERSE

    // Decrement the number and then return it's new value
    println(--number) // 11

    // Return the current value and then decrement the number.
    println(number--) // 11

    println(number) // 10
}